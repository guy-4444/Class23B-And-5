package com.guy.class23b_and_5.objects;

public class SunModel {

    private int sunrise;
    private int sunset;

    public SunModel() {}

    public int getSunrise() {
        return sunrise;
    }

    public void setSunrise(int sunrise) {
        this.sunrise = sunrise;
    }

    public int getSunset() {
        return sunset;
    }

    public void setSunset(int sunset) {
        this.sunset = sunset;
    }
}
