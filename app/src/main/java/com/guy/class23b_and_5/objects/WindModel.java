package com.guy.class23b_and_5.objects;

public class WindModel {

    private double speed;
    private int deg;

    public WindModel() {}

    public double getSpeed() {
        return speed;
    }

    public void setSpeed(double speed) {
        this.speed = speed;
    }

    public int getDeg() {
        return deg;
    }

    public void setDeg(int deg) {
        this.deg = deg;
    }
}
